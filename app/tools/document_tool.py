def _clean_markdown_preserving_formatting(text: str) -> str:
    """Remove code blocks and links, but preserve bold and italic markers."""
    text = re.sub(r'`(.+?)`', r'\1', text)
    text = re.sub(r'\[(.+?)\]\(.+?\)', r'\1', text)
    return text.strip()


def _clean_all_markdown_for_tables(text: str) -> str:
    """Remove code blocks, links, bold, and italic markers completely for tables."""
    text = re.sub(r'`(.+?)`', r'\1', text)
    text = re.sub(r'\[(.+?)\]\(.+?\)', r'\1', text)
    text = text.replace('**', '').replace('*', '')
    return text.strip()


def _break_long_words(text: str, max_length: int = 50) -> str:
    """Break long sequences of non-whitespace characters to prevent PDF rendering errors."""
    return re.sub(r'(\S{' + str(max_length) + r'})', r'\1 ', text)


def _add_content_sections(doc: Document, content: str) -> None:
    """Parse and add content sections to the document.

    Handles headings (## / ###), bullet points (- / *), numbered lists,
    and tables (| ... |).
    """
    lines = content.split("\n")
    i = 0
    in_table = False
    table_rows: list[list[str]] = []

    while i < len(lines):
        line = lines[i].strip()

        # Skip empty lines
        if not line:
            if in_table and table_rows:
                _add_table(doc, table_rows)
                table_rows = []
                in_table = False
            i += 1
            continue

        # Table detection
        if line.startswith("|") and line.endswith("|"):
            # Skip separator rows
            if re.match(r'^\|[\s\-:|]+\|$', line):
                i += 1
                continue
            cells = [c.strip() for c in line.strip("|").split("|")]
            # Filter out empty cells at the start and end due to leading/trailing |
            if cells and cells[0] == "":
                cells = cells[1:]
            if cells and cells[-1] == "":
                cells = cells[:-1]
            table_rows.append(cells)
            in_table = True
            i += 1
            continue

        # Flush any pending table
        if in_table and table_rows:
            _add_table(doc, table_rows)
            table_rows = []
            in_table = False

        # Heading level 1 (##)
        if line.startswith("## "):
            heading_text = line[3:].strip()
            # Clean any markdown bold markers
            heading_text = heading_text.replace("**", "")
            doc.add_heading(heading_text, level=1)
            i += 1
            continue

        # Heading level 2 (###)
        if line.startswith("### "):
            heading_text = line[4:].strip()
            heading_text = heading_text.replace("**", "")
            doc.add_heading(heading_text, level=2)
            i += 1
            continue

        # Heading level 3 (####)
        if line.startswith("#### "):
            heading_text = line[5:].strip()
            heading_text = heading_text.replace("**", "")
            doc.add_heading(heading_text, level=3)
            i += 1
            continue

        # Bullet points
        if line.startswith("- ") or line.startswith("* "):
            text = line[2:].strip()
            text = _clean_markdown(text)
            _add_paragraph_with_formatting(doc, text, style='List Bullet')
            i += 1
            continue

        # Numbered list (e.g., "1. ", "2. ")
        match = re.match(r'^(\d+)\.\s+(.+)', line)
        if match:
            text = _clean_markdown(match.group(2))
            _add_paragraph_with_formatting(doc, text, style='List Number')
            i += 1
            continue

        # Regular paragraph
        text = _clean_markdown(line)
        if text:
            _add_paragraph_with_formatting(doc, text)

        i += 1

    # Flush final table
    if table_rows:
        _add_table(doc, table_rows)


def _add_paragraph_with_formatting(doc: Document, text: str, style=None):
    """Add a paragraph while parsing inline bold markdown."""
    p = doc.add_paragraph(style=style)
    parts = re.split(r'(\*\*.*?\*\*)', text)
    for part in parts:
        if part.startswith('**') and part.endswith('**'):
            run = p.add_run(part[2:-2])
            run.bold = True
        else:
            p.add_run(part)
    return p


def _add_table(doc: Document, rows: list[list[str]]) -> None:
    """Add a table to the document."""
    if not rows:
        return

    num_cols = max(len(r) for r in rows)
    if num_cols == 0:
        return
        
    table = doc.add_table(rows=len(rows), cols=num_cols)
    table.style = 'Light Shading Accent 1'

    for r_idx, row_data in enumerate(rows):
        for c_idx, cell_text in enumerate(row_data):
            if c_idx < num_cols:
                cell = table.cell(r_idx, c_idx)
                cell.text = "" # Clear default
                p = cell.paragraphs[0]
                text = _clean_markdown(cell_text)
                
                parts = re.split(r'(\*\*.*?\*\*)', text)
                for part in parts:
                    if part.startswith('**') and part.endswith('**'):
                        run = p.add_run(part[2:-2])
                        run.bold = True
                    else:
                        p.add_run(part)
                        
                # Bold header row
                if r_idx == 0:
                    for paragraph in cell.paragraphs:
                        for run in paragraph.runs:
                            run.bold = True

    doc.add_paragraph()  # Spacer after table


def _clean_markdown(text: str) -> str:
    """Remove common markdown formatting from text, preserving bold."""
    # Remove inline code
    text = re.sub(r'`(.+?)`', r'\1', text)
    # Remove markdown links, keep text
    text = re.sub(r'\[(.+?)\]\(.+?\)', r'\1', text)
    return text.strip()


def _editorial_cleanup_pass(text: str) -> str:
    """Lightweight editorial pass to fix formatting, spacing, and punctuation issues."""
    if not text:
        return ""
    # 1. Remove repeated periods
    text = re.sub(r'\.{2,}', '.', text)
    # 2. Remove duplicate commas
    text = re.sub(r',{2,}', ',', text)
    # 3. Correct spacing around punctuation: space after punctuation, no space before
    text = re.sub(r'\s+([.,;:!?])', r'\1', text)
    # Ensure space after punctuation (but not if followed by a number or punctuation)
    text = re.sub(r'([.,;:!?])(?=[A-Za-z])', r'\1 ', text)
    # 4. Clean up typical spelling or spacing bugs
    text = re.sub(r'\bcore\s+issueunknown\b', 'core issue: unknown', text)
    # 5. Ensure headings don't have trailing punctuation like periods
    lines = []
    for line in text.split("\n"):
        stripped = line.strip()
        if stripped.startswith("#"):
            if stripped.endswith("."):
                line = line.rstrip(".")
        lines.append(line)
    text = "\n".join(lines)
    return text
